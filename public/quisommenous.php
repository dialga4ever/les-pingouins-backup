<div class="corps">Ce site trouve ses origines dans les recherches généalogiques entreprises par Philippe Boutelier.</br>

La généalogie n’est pas pour lui une course à l’arbre le plus gros , il  préfère s’intéresser aux personnes et à leur vie. C’est ainsi que, en 2012, il se penche sur l’histoire de  Joseph Taylor, <strong>sauveteur du Dunkerquois</strong>, grand-oncle préféré de son père.</br>
ll s’aperçoit alors, à travers la carrière de ce sauveteur que  l’histoire de ces personnes d’exception (de Bray-Dunes à Grand-Fort-Philippe) dort dans les archives de la Bibliothèque Nationale de France.</br>
Il décide de mettre en lumière le parcours de ces hommes guidés par l’abnégation, l’humanisme et le courage,  avec un mépris indéniable pour les  risques encourus.</br>

D’abord hébergées   par l’association Généalogie Histoire du Dunkerquois (GHDK), les données collectées ne peuvent cependant pas être suffisamment mises en valeur et ce sera la création du site  des « Sauveteurs du Dunkerquois », site indépendant permettant une interactivité entre les pages.</br>

Un premier site est développé mais est vite rattrapé par le volume des données disponibles. Un changement logiciel s’impose. Le 25 octobre 2021 celui-ci voit le jour.</br></br></br>



<strong>« Les Corsaires Dunkerquois »</strong> : l’association philanthropique dunkerquoise remarque ce travail de mémoire et passe une convention avec Philippe Boutelier de façon à couvrir les frais inhérents au site.</br>
Par cette convention, où l’association devient propriétaire du site, la pérennité mémorielle de l’histoire des sauveteurs du dunkerquois est assurée.</br></br>

<strong>La station SNSM </strong>de Dunkerque, en ouvrant  ses archives, contribue au développement de ce site.</br>

Philippe Boutelier est intégré à la station SNSM de Dunkerque avec le titre de sauveteur de mémoires.</br>



Ce travail n’a pu, ne peut et ne pourra être réalisé sans un réseau autour de Philippe Boutelier. Il faut citer Lysiane Varlet pour les travaux de relecture, Hélène Berthou, Régis Brunet pour les travaux de saisie des informations, Odile T’Servrancx pour les recherches aux archives, Alain Derenoncourt pour ses analyses maritimes des textes, Gérard Bollengier pour ses recherches généalogiques, les contributeurs de la page Facebook « Dunkerque et son histoire » ainsi que les descendants de sauveteurs qui ont bien voulu confier des documents familiaux pour monter les pages individuelles et abonder les récits du site.</br></br>

Monsieur Marcq du siège de la SNSM pour ses communications et ses réponses au questionnement du rédacteur.</div>
