<div class="corps">La présence de bancs de sable a toujours rendu la navigation périlleuse  au large du dunkerquois.</br>

Si ces bancs ont rendu moult services à l’incontournable Jean Bart, ils sont aussi responsables de nombreux naufrages.</br>

Faisant abstraction des dangers, de vaillants marins ont tout fait afin d’en réduire les conséquences, sauvant un grand nombre de personnes.</br>

Vous trouverez le récit de leurs exploits dans les sorties en mer recensées entre 1739 et 2021 dans l’état actuel des données. Dans ces textes, certaines tournures peuvent paraître archaïques , nous n’avons pas voulu les modifier pour garder la simplicité et l’authenticité de ces témoignages.</br>

On parle de sorties en mer car toutes ces missions ne se soldent pas forcément par un sauvetage. (refus d’être assisté, fausse alerte….)</div>
